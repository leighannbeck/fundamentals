/* DATA TYPES

Primitive Types: basic data types
    --boolean (true or false)
    --null (set to nothing by user)
    --undefined (set to nothing by JS)
    --number (int - whole number, float - decimal number, double - float with 2x decimal places)
    --string ("string of letters, words, numbers as a single data type")

Composite Types: combines 2 or more primitive types
    --object ()

let obj = {
  key1: "value",
  key2: 4,
  boolean: true,
  obj2: {
    obj2Key1: "Inner Object Value"
    },
};

console.log(obj.obj2.obj2Key1);

*/

/* TYPING SYSTEMS

Evaluation system: Static or Dynamic
    --Static Systems: programmer will tell the language what data type is being used
        let variable : string = "string";

    --Dynamic Systems: language is able to recognize the data type on it's own

Strong or Weak Systems: controls how data types will be converted if different data types are used.
    --Strong Systems: will not be able to convert data types to the same type
    --Weak Systems: system will attempt to make a conversion, but may not always go as planned.
        let var1 = "2";
        let var2 = 5;
        let result = var1 + var2;

        console.log(result);
 */

